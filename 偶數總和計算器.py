def 計算偶數總和(數字列表):
    """
    接收一個數字列表，回傳其中所有偶數的總和。
    
    參數:
    數字列表 (list): 包含數字的列表。
    
    回傳:
    int 或 float: 列表中所有偶數的總和。
    """
    總和 = 0
    for 數字 in 數字列表:
        if 數字 % 2 == 0:
            總和 += 數字
    return 總和

