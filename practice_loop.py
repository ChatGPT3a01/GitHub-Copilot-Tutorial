# 請輸入 1 到 10 的數字: 15
# 請輸入 1 到 10 的數字: 0
# 請輸入 1 到 10 的數字: 5
# 你輸入的數字是: 5

def practice7():
    """輸入檢驗 - 要求輸入有效的數字"""
    while True:
        try:
            num = int(input("請輸入 1 到 10 的數字: "))
            if 1 <= num <= 10:
                print(f"你輸入的數字是: {num}")
                break
            else:
                print("請輸入 1 到 10 之間的數字")
        except ValueError:
            print("請輸入一個有效的數字")


